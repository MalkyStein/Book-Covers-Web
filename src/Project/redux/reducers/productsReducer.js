 const ProductReducer = (state = products, action) => {
    switch (action.type) {

        
        case "ADD_SUM":
            
            debugger
            const products = [...state]
            if( products.find((p)=>p.code===action.code).qty>0){
                products.find((p)=>p.code===action.code).qty--

                }
                if(products.find((p)=>p.code===action.code).qty<=0)
                {
                    alert("The item out of stock")
                }
            return products

        case "ADD_QTY":
{
        const products=[...state]
        products.find((p)=>p.code===action.code).qty+=1
        products.find((p)=>p.code===action.code).amount-=1
        
        return products
    }
        default:
            return state
    }
    

}
